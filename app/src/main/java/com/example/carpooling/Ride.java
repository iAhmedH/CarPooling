package com.example.carpooling;

public class Ride {
    private String location;
    private String name;
    private String time;
    private String driver;

    private String price;

    private String seats;

    // Constructors...

    // Getter and Setter methods...

    // Getter method for the "location" attribute
    public String getLocation() {
        return location;
    }

    // Setter method for the "location" attribute
    public void setLocation(String location) {
        this.location = location;
    }

    // Getter method for the "name" attribute
    public String getName() {
        return name;
    }

    // Setter method for the "name" attribute
    public void setName(String name) {
        this.name = name;
    }

    // Getter method for the "time" attribute
    public String getTime() {
        return time;
    }

    // Setter method for the "time" attribute
    public void setTime(String time) {
        this.time = time;
    }

    // Getter method for the "driver" attribute
    public String getDriver() {
        return driver;
    }

    // Setter method for the "driver" attribute
    public void setDriver(String driver) {
        this.driver = driver;
    }

    public String getPrice() {
        return price;
    }

    // Setter method for the "driver" attribute
    public void setPrice(String price) {
        this.price = price;
    }

    public String getSeats() {
        return seats;
    }

    // Setter method for the "driver" attribute
    public void setSeats(String seats) {
        this.seats = seats;
    }
}